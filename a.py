# 練習01 計算する(ex01.py)
# 　整数、5 と整数、2 の２つの数の計算を行なうプログラムを書きなさい。計算は足し算、引き算、かけ算、割り算の商と余り、実数の割り算の５つが答えとして出るようにしなさい。
# 実行結果
# 5 ＋ 2 = 7
# 5 − 2 = 3
# 5 × 2 = 10
# 5 ÷ 2 = 2 余り 1
# 5 ÷ 2 = 2.5


a=5
b=2

print(a,'+',b,'=',a+b)
print(a,'-',b,'=',a-b)
print(a,'✖️',b,'=',a*b)
print(a,'÷',b,'=',a/b)
print(a,'÷',b,'=',a//b ,' amari ',a%b)
 